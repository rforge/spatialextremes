Currently, the SpatialExtremes package models spatial extremes by
fitting max-stable processes to data using composite likelihood
maximisation. Many tools to characterize the spatial behaviour are
also implemented.

<p>
In a nutshell, the SpatialExtremes packages can:
<ul>
  <li>Fit max-stable processes to data</li>
  <li>Estimate the extremal coefficient</li>
  <li>Allow for prediction</li>
  <li>Fits penalized splines with radial basis functions</li>
  <li>Plot the covariance and the madogram to better understand how
  evolve dependence in space</li>
</ul>

