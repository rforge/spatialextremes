    <h1>What is SpatialExtremes?</h1>
    <div class="text">
      SpatialExtremes is an add-on package for the statistical
      software <a href="http://www.r-project.org/">R</a>.  Its goal is
      to provides tools for the statistical modelling of spatial
      extremes. Why bother about spatial extremes? The areal modelling
      of the extremes of a natural process such as rainfall or
      temperature is important in environmental statistics; for
      example, understanding extreme areal rainfall is crucial in
      flood protection. Nor the univariate neither the multivariate
      extreme value theory will be useful in such situation as most of
      the environmental processes are spatial in extent. This software
      provides recent the statistical models for spatial extremes. The
      main types of statistical models thus far implemented are based
      on latent variables, on copulas and on max-stable processes.
    </div>

    <h1>How do I install it?</h1>
    <div class="text">
      You have secveral possibilities. As SpatialExtremes is available
      on CRAN you can install it directly from an R session. If you
      feel more risky then you can get
      the <a href="https://r-forge.r-project.org/R/?group_id=265">last
      development version</a> from
      the <a href="https://r-forge.r-project.org/">r-forge</a>
      system.
    </div>

